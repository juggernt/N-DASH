  	N-DASH is a lightweight, responsive, web-based user interface for Novo users who run a wallet, node, witnessing, mining account, or all of the above on a Linux server (i.e. a Raspberry Pi). Using this dashboard users can keep an eye on their Novo server and control their nodes, holding and mining account without the need of a terminal.
